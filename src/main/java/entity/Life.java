package entity;

public class Life {
    private int life_id;
    private String life_name;
    private int life_numer;
    public Life() {
    }

    public int getLife_id() {
        return life_id;
    }

    public void setLife_id(int life_id) {
        this.life_id = life_id;
    }

    public String getLife_name() {
        return life_name;
    }

    public void setLife_name(String life_name) {
        this.life_name = life_name;
    }

    public int getLife_numer() {
        return life_numer;
    }

    public void setLife_numer(int life_numer) {
        this.life_numer = life_numer;
    }
}
